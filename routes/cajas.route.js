/** =====================================================================
 *  CAJAS ROUTER
=========================================================================*/
const { Router } = require('express');
const { check } = require('express-validator');

// MIDDLEWARES
const { validarCampos } = require('../middlewares/validar-campos');
const { validarJWT } = require('../middlewares/validar-jwt');

// CONTROLLER
const { getCajas, createCaja, updateCaja, deleteCaja } = require('../controllers/cajas.controller');

const router = Router();

/** =====================================================================
 *  GET CAJAS
=========================================================================*/
router.get('/', validarJWT, getCajas);
/** =====================================================================
 *  GET CAJAS
=========================================================================*/

/** =====================================================================
 *  CREATE CAJAS
=========================================================================*/
router.post('/', [
        validarJWT,
        check('name', 'El nombre es obligatorio').not().isEmpty(),
        validarCampos
    ],
    createCaja
);
/** =====================================================================
 *  CREATE CAJAS
=========================================================================*/

/** =====================================================================
 *  UPDATE CAJAS
=========================================================================*/
router.put('/:id', [
        validarJWT,
        check('name', 'El nombre es obligatorio').not().isEmpty(),
        validarCampos
    ],
    updateCaja
);
/** =====================================================================
 *  UPDATE CAJAS
=========================================================================*/

/** =====================================================================
 *  DELETE CAJAS
=========================================================================*/
router.delete('/:id', validarJWT, deleteCaja);
/** =====================================================================
 *  DELETE CAJAS
=========================================================================*/

// EXPORTS
module.exports = router;